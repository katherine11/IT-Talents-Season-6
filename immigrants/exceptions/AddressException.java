package immigrants.exceptions;

public class AddressException extends Exception {

	private static final long serialVersionUID = -6744724154196634501L;

	public AddressException() {}

	public AddressException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AddressException(String arg0) {
		super(arg0);
	}

	public AddressException(Throwable arg0) {
		super(arg0);
	}
	
	

}
