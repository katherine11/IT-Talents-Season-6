package immigrants.exceptions;

public class ExtremistImmigrantException extends ImmigrantException {

	private static final long serialVersionUID = 2611182339313231233L;

	public ExtremistImmigrantException() {}

	public ExtremistImmigrantException(String message, Throwable cause) {
		super(message, cause);
	}

	public ExtremistImmigrantException(String message) {
		super(message);
	}

	public ExtremistImmigrantException(Throwable cause) {
		super(cause);
	}

	
	
}
